          <ul style="font-size: 18px;">
            <li>Find a Quiet Space: Choose a quiet and clutter-free space where you can practice yoga comfortably. A yoga mat or a non-slip surface is recommended to prevent slipping during poses.</li>
            <li>Warm Up: Begin with a gentle warm-up to prepare your body for the exercises. You can start with some simple stretches like neck rolls, shoulder rolls, and gentle twists.</li>
            <li>Practice Breathing: Yoga is often accompanied by controlled breathing. Focus on deep, slow breaths throughout the practice, inhaling through your nose and exhaling through your mouth.</li>

            <br>
            <p>Start with Basic Poses:</p>

            <li>Mountain Pose (Tadasana): Stand tall with your feet together, arms at your sides, and palms facing forward. Breathe deeply and feel the grounding sensation through your feet.</li>
            <li>Downward Dog (Adho Mukha Svanasana): From a tabletop position (hands and knees), lift your hips up and back, forming an inverted V-shape. Press your palms into the mat and extend your spine and legs.</li>
            <li>Child's Pose (Balasana): Kneel on the floor, sit back on your heels, and stretch your arms forward. Rest your forehead on the mat or a block.</li>
            <li>Flow through Sun Salutations: Sun Salutations are a sequence of poses that create a fluid movement. They are excellent for warming up the body and building strength and flexibility. A basic Sun Salutation sequence typically includes Mountain Pose, Forward Fold, Plank Pose, Chaturanga, Upward Dog, and Downward Dog.</li>
            <li>Explore Standing Poses: Standing poses like Warrior I, Warrior II, and Triangle Pose are great for building strength in the legs and improving balance.</li>
            <li>Work on Balance Poses: Poses like Tree Pose (Vrksasana) and Eagle Pose (Garudasana) challenge your balance and help improve stability.</li>
            <li>Focus on Backbends and Forward Bends: Poses like Cobra Pose (Bhujangasana) and Seated Forward Bend (Paschimottanasana) stretch and strengthen the back and hamstrings.</li>
            <li>Include Twists: Twisting poses like Half Lord of the Fishes (Ardha Matsyendrasana) improve spinal mobility and aid in digestion.</li>
            <li>End with Savasana: Lie down on your back, relax your body completely, and take a few minutes in Savasana (Corpse Pose) to allow your body and mind to relax and integrate the benefits of your practice.</li>
